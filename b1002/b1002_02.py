# 숫자를 입력받아 100보다 큰 수인지 작은 수인지 출력
num = int(input("숫자를 입력하시오"))
if num > 100 :
  print("100보다 큰 수입니다.")
elif num < 100 :
  print("100보다 작은 수입니다.")
else :
  print("100입니다.")
  
